public class DataTypes {
    public static void main(String[] args){
        byte smallNumber = 10;
        short shortNumber = 200;
        int number = 1000;
        long bigNumber = 100000L;
        float decimalNumber = 12.5f;
        double bigDecimalNumber = 12.56789;
        boolean isTrue = true;
        char letter = 'A';
        System.out.println("Small Number: " + smallNumber);
        System.out.println("Short Number: " + shortNumber);
        System.out.println("Number: " + number);
        System.out.println("Big Number: " + bigNumber);
        System.out.println("Decimal Number: " + decimalNumber);
        System.out.println("Big Decimal Number: " + bigDecimalNumber);
        System.out.println("Is True: " + isTrue);
        System.out.println("Letter: " + letter);
    }
}
